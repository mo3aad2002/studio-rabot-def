import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("..", import.meta.url));
const collator = new Intl.Collator(undefined, { numeric: true, sensitivity: "base" });
const IMAGE_RE = /\.(jpe?g|png|webp|gif)$/i;
const NUMBER_RE = /(\d+(?:\.\d+)?)/;

function compareImageNames(a, b) {
  const numA = a.match(NUMBER_RE);
  const numB = b.match(NUMBER_RE);
  if (numA && numB) {
    const diff = parseFloat(numA[1]) - parseFloat(numB[1]);
    if (diff !== 0) return diff;
  } else if (numA) {
    return -1;
  } else if (numB) {
    return 1;
  }
  return collator.compare(a, b);
}

function cleanTitle(folder) {
  let title = folder.replace(/\s+/g, " ").trim();
  let credit;

  const creditMatch = title.match(/\s*\+\s*vermeld foto'?s ism\s+(.+)$/i);
  if (creditMatch) {
    credit = creditMatch[1].trim();
    title = title.slice(0, creditMatch.index).trim();
  }

  const titleMatch = title.match(/\s*\+\s*titel\s+(.+)$/i);
  if (titleMatch) {
    title = titleMatch[1].trim();
  } else {
    title = title.charAt(0).toUpperCase() + title.slice(1);
  }

  return { title, credit };
}

function scanCategory(dirName) {
  const dirPath = join(root, "public", dirName);

  const folders = readdirSync(dirPath, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => collator.compare(a, b));

  return folders.map((folder) => {
    const folderPath = join(dirPath, folder);
    const images = readdirSync(folderPath, { withFileTypes: true })
      .filter((entry) => entry.isFile() && IMAGE_RE.test(entry.name))
      .map((entry) => entry.name)
      .sort(compareImageNames);

    const { title, credit } = cleanTitle(folder);
    return { folder, title, credit, images };
  });
}

function serializeEntries(entries) {
  return entries
    .map((entry) => {
      const credit = entry.credit ? `, credit: ${JSON.stringify(entry.credit)}` : "";
      const images = entry.images.map((image) => JSON.stringify(image)).join(", ");
      return `  { folder: ${JSON.stringify(entry.folder)}, title: ${JSON.stringify(entry.title)}${credit}, images: [${images}] },`;
    })
    .join("\n");
}

const projecten = scanCategory("SELECTIE PROJECTEN");
const schetsontwerpen = scanCategory("SELECTIE SCHETSONTWERPEN");

const output = `// Generated by scripts/generate-projects-data.mjs -- run \`npm run sync:projects\` to regenerate.

export interface ProjectEntry {
  folder: string;
  title: string;
  credit?: string;
  images: string[];
}

export const PROJECTEN_DIR = "SELECTIE PROJECTEN";
export const SCHETSONTWERPEN_DIR = "SELECTIE SCHETSONTWERPEN";

export function projectImageUrl(dir: string, folder: string, file: string): string {
  return encodeURI(\`/\${dir}/\${folder}/\${file}\`);
}

export const projecten: ProjectEntry[] = [
${serializeEntries(projecten)}
];

export const schetsontwerpen: ProjectEntry[] = [
${serializeEntries(schetsontwerpen)}
];
`;

writeFileSync(join(root, "src", "data", "projects.ts"), output, "utf-8");
console.log(`Wrote ${projecten.length} projecten and ${schetsontwerpen.length} schetsontwerpen entries.`);
